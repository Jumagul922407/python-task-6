# Тизмелер менен иштөө
fruits = ["алма", "банан", "алмурут", "ананас"]

# Тизменин элементтерин чыгаруу
for fruit in fruits:
    print("Мага жаккан жемиш:", fruit)

# Тизменин узундугун чыгаруу
print("Жемиштердин саны:", len(fruits))

# Жаңы жемиш кошуу
fruits.append("өрүк")
print("Жаңыланган тизме:", fruits)
